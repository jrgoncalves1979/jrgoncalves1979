//Soma de variaveis de numeros inteiros.
var x = 20; //Numero inteiro.
var y = 21;

var total = x + y;

alert(total);



//Junção de duas variaveis do tipo String com concatenação.
var nome = "Junior"; //Entre aspas vira String, Ex: se colocar "10" deixa de ser numero e vira uma String.
var sobrenome = "Gonçalves";

var nomesobrenome = nome+" "+sobrenome; //  +" "+ concateção, espaço vazio para separar os dois nomes nesse caso, sem concatenação var nomesobrenome = nome + sobrenome;

alert(nomesobrenome);



//Junção de String com numero inteiro.
var nome = "Junior";
var idade = 41;

var total = nome + idade; //Com concatenação var total = nome+" "+idade;

alert(total);



//Strings em formato de numeros, se juntam não somam.
var x = "20"; //Numero inteiro.
var y = "21";

var total = x + y;

alert(total);



















































































